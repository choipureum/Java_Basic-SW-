//	+특수기호는 _와 $만 허용
//	+대소문자를 구분(다르다)
//	+숫자로 시작하면 안된다
//	+띄어쓰기(X)
//	+예약어 사용 불가
//----------------관례상 지켜야하는 것들(에러X)
//	+변수명과 패키지명, 메소드명은 소문자로 시작
//	+클래스명과 프로젝트명은 대문자로 시작
