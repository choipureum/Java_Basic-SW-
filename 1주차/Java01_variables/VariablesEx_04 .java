//	<변수 명명규칙(Naming 규약)>
//-식별자의 이름을 정할 때 지켜야하는 사항들 
//-변수명, 메소드명, 패키지명, 클래스명, 프로젝트명등의 이름을 붙일 때 지켜야하는 규칙
//----------------반드시 지켜야하는 것들(에러)
