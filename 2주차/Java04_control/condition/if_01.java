package Java04_control.condition;
import java.util.*;

public class if_01 {
	public static void main(String[] args) {
		//조건문- if
		//if(조건){}
		//조건식은 연산의 결과가 true/false로 판별가능한 연산식
		if(false) {
			System.out.println("출력될까???");
		}
		System.out.println("여기는 if 바깥입니다");
		//		< 제어문, Control Statement >
//		 -기본적인 프로그램의 정상적인 흐름을 제어하는 구문
//		 (정상적인 흐름 : main() 메소드에서 시작하여 코드 한 줄 씩 끝가지 수행되는 것)
//		 -분기한다라고도 함
//		 -프로그램의 실행흐름을 개발자가 원하는 방향으로 제어
//		 
//		 < 제어문의 종류 >
//		  +조건문,Condition
//
//			-if
//			-else if
//			-else
//
//		  +반복문,Loop
//
//			-for
//			-while
//			-do-while
//
//		  +선택문
//
//			-switch - case
	}
}
