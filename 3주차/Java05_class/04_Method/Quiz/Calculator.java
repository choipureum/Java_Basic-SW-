package Java06_class.method.quiz;

import java.util.*;
public class Calculator {
	public int sum(int n1, int n2) {
		return n1+n2;
	}
	public int sub(int n1, int n2) {
		return n1-n2;
	}
	public double div(int n1, int n2) {
		return (double)n1/n2;
	}
	public int mul(int n1, int n2) {
		return n1*n2;
	}
}
